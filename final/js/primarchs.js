 function searchByName() {
        const input = document.getElementById('searchInput').value.trim();
        const resultDiv = document.getElementById('result');
        resultDiv.innerHTML = ''; 

        if (input === '') {
            resultDiv.innerText = 'Здесь надо написать имя примарха';
            return;
        }

        let result = '';
        let found = false;

        for (let i = 0; i < primarchs.length; i++) {
            if (primarchs[i].primarchName.toLowerCase().includes(input.toLowerCase())) {
                found = true;
                result += primarchs[i].legionNumber + " " + primarchs[i].legionName + " \n" + 
                          primarchs[i].primarchName + " \n" + 
                          primarchs[i].loyality + "\n" + 
                          primarchs[i].liveStatus + " " + primarchs[i].addLiveStatus + "\n";
            }
        }

        if (found) {
            resultDiv.innerText = 'Вот твой красавец:\n' + result;
        } else {
            resultDiv.innerText = 'Таких тут нет';
        }
    }

    document.getElementById('searchButton').addEventListener('click', searchByName);
      

    
    
